# for types of processes

# for running procsses 
ps -r

# processes not associated with terminal
ps -a

# process owned by user 
ps -x

# processes with full format listing
ps -f

# view processes 
ps -e

# to find pid of a process 
pidof "processname"

# to bring processes stopped in the background to foreground
fg %1

# to send process to background
bg %1
